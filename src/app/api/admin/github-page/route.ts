// ─── POST: create page ────────────────────────────────────────────────────────

export async function POST(req: NextRequest) {
  const session = await getAdminSession();
  if (!session) return NextResponse.json({ error: 'unauthorized' }, { status: 401 });
  if (!GITHUB_TOKEN || !GITHUB_REPO) return NextResponse.json({ error: 'GITHUB_TOKEN und GITHUB_REPO env vars fehlen' }, { status: 500 });

  const body = await req.json().catch(() => ({}));
  const { label, slug, source_page } = body;
  if (!label || !slug) return NextResponse.json({ error: 'label und slug erforderlich' }, { status: 400 });

  const cleanSlug = slug.replace(/[^a-z0-9-]/g, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
  const pageId    = cleanSlug;
  const path      = `/p/${cleanSlug}`;
  const filePath  = `src/app/p/${cleanSlug}/page.tsx`;

  // Check for duplicate
  const { data: existing } = await supabase.from('cms_pages').select('id').eq('id', pageId).maybeSingle();
  if (existing) return NextResponse.json({ error: `Seite "${cleanSlug}" existiert bereits` }, { status: 409 });

  // 1. Register in Supabase
  const { data: newPage, error: pageError } = await supabase
    .from('cms_pages').insert({ id: pageId, label, path, is_system: false }).select().single();
  if (pageError) return NextResponse.json({ error: pageError.message }, { status: 500 });

  // 2. Copy sections + content from source if duplicating
  if (source_page) {
    const { data: sourceSections } = await supabase
      .from('cms_sections').select('*').eq('page_id', source_page).order('sort_order');
    const instanceMap: Record<string, string> = {};
    if (sourceSections?.length) {
      const newSections = sourceSections.map((s, idx) => {
        const ts  = (Date.now() + idx).toString(36);
        const rnd = Math.random().toString(36).slice(2, 6);
        const newInst = `${s.section_type}_${ts}_${rnd}`;
        instanceMap[s.section_instance] = newInst;
        return { page_id: pageId, section_instance: newInst, section_type: s.section_type, label: s.label, sort_order: s.sort_order, hidden: s.hidden };
      });
      await supabase.from('cms_sections').insert(newSections);
    }
    const { data: srcContent } = await supabase.from('cms_content').select('*').eq('page', source_page);
    if (srcContent?.length) {
      await supabase.from('cms_content').insert(
        srcContent.map(c => ({ page: pageId, section_key: instanceMap[c.section_key] ?? c.section_key, field_key: c.field_key, value: c.value, updated_at: new Date().toISOString() }))
      );
    }
  }

  // 3. Write file to GitHub
  try {
    await ghPut(filePath, `CMS: Neue Seite "${label}" (${cleanSlug})`, generatePageFile(pageId, label));
  } catch (err: any) {
    // Rollback Supabase on GitHub failure
    await supabase.from('cms_content').delete().eq('page', pageId);
    await supabase.from('cms_sections').delete().eq('page_id', pageId);
    await supabase.from('cms_pages').delete().eq('id', pageId);
    return NextResponse.json({ error: `GitHub Fehler: ${err.message}` }, { status: 500 });
  }

  return NextResponse.json({ ok: true, data: newPage, path });
}

// ─── DELETE: remove page ──────────────────────────────────────────────────────

export async function DELETE(req: NextRequest) {
  const session = await getAdminSession();
  if (!session) return NextResponse.json({ error: 'unauthorized' }, { status: 401 });
  if (!GITHUB_TOKEN || !GITHUB_REPO) return NextResponse.json({ error: 'GITHUB_TOKEN und GITHUB_REPO env vars fehlen' }, { status: 500 });

  const { id } = await req.json().catch(() => ({}));
  if (!id) return NextResponse.json({ error: 'id erforderlich' }, { status: 400 });

  const { data: page } = await supabase.from('cms_pages').select('*').eq('id', id).single();
  if (!page) return NextResponse.json({ error: 'nicht gefunden' }, { status: 404 });
  if (page.is_system) return NextResponse.json({ error: 'Systemseiten können nicht gelöscht werden' }, { status: 403 });

  // 1. Delete from GitHub
  const filePath = `src/app/p/${id}/page.tsx`;
  const existing = await ghGet(filePath);
  if (existing?.sha) {
    try {
      await ghDelete(filePath, `CMS: Seite "${page.label}" gelöscht`, existing.sha);
    } catch (err: any) {
      return NextResponse.json({ error: `GitHub Fehler: ${err.message}` }, { status: 500 });
    }
  }

  // 2. Clean Supabase
  await Promise.all([
    supabase.from('cms_content').delete().eq('page', id),
    supabase.from('cms_sections').delete().eq('page_id', id),
  ]);
  await supabase.from('cms_pages').delete().eq('id', id);

  return NextResponse.json({ ok: true });
}
