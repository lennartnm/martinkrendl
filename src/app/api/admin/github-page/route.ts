// ─── GitHub API helpers ───────────────────────────────────────────────────────

async function ghGet(path: string) {
  const res = await fetch(`https://api.github.com/repos/${GITHUB_REPO}/contents/${path}?ref=${GITHUB_BRANCH}`, {
    headers: { Authorization: `token ${GITHUB_TOKEN}`, Accept: 'application/vnd.github.v3+json' },
    cache: 'no-store',
  });
  return res.ok ? res.json() : null;
}

async function ghPut(filePath: string, message: string, fileContent: string, sha?: string) {
  const body: Record<string, string> = {
    message,
    content: Buffer.from(fileContent, 'utf-8').toString('base64'),
    branch: GITHUB_BRANCH,
  };
  if (sha) body.sha = sha;
  const res = await fetch(`https://api.github.com/repos/${GITHUB_REPO}/contents/${filePath}`, {
    method: 'PUT',
    headers: {
      Authorization: `token ${GITHUB_TOKEN}`,
      Accept: 'application/vnd.github.v3+json',
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(body),
  });
  const json = await res.json();
  if (!res.ok) throw new Error(json.message || 'GitHub PUT failed');
  return json;
}

async function ghDelete(filePath: string, message: string, sha: string) {
  const res = await fetch(`https://api.github.com/repos/${GITHUB_REPO}/contents/${filePath}`, {
    method: 'DELETE',
    headers: {
      Authorization: `token ${GITHUB_TOKEN}`,
      Accept: 'application/vnd.github.v3+json',
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ message, sha, branch: GITHUB_BRANCH }),
  });
  const json = await res.json();
  if (!res.ok) throw new Error(json.message || 'GitHub DELETE failed');
  return json;
}

